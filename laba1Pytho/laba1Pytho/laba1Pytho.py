chislo = int(input("Введите число: "))
pogchislo = 12
if chislo == pogchislo:
    print("Введено пограничное число!")
if chislo < pogchislo:
    print("1ое число меньше пограничного")
if chislo > pogchislo:
    print("1ое число больше пограничного")
if chislo > pogchislo*3:
    print("1ое число больше пограничного более, чем в 3 раза")
